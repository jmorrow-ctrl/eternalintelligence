function extractVisuals(text: string): { word: string; emoji: string }[] {
  const lower = text.toLowerCase();
  const found: { word: string; emoji: string }[] = [];
  const seen = new Set<string>();

  for (const [word, emoji] of VOCAB_MAP) {
    if (seen.has(emoji)) continue;
    if (lower.includes(word)) {
      found.push({ word, emoji });
      seen.add(emoji);
    }
  }

  return found;
}

export function VisualCues({ prompt }: { prompt: string }) {
  const visuals = extractVisuals(prompt);
  if (visuals.length === 0) return null;

  return (
    <div className="visual-cues">
      {visuals.slice(0, 6).map((v, i) => (
        <div key={i} className="visual-cue" style={{ animationDelay: `${i * 0.1}s` }}>
          <span className="visual-emoji">{v.emoji}</span>
          <span className="visual-word">{v.word}</span>
        </div>
      ))}
    </div>
  );
}
